// ----------------------------------------------------------------------
// 9. Различные сортировки на массиве ['3'=>'a','1'=>'c','2'=>'e','4'=>'b']
// ----------------------------------------------------------------------
echo "<h2>Задание 9</h2>";
$original = ['3' => 'a', '1' => 'c', '2' => 'e', '4' => 'b'];
echo "Исходный массив: ";
print_r($original);
echo "<br>";

// asort – сортировка по значениям (сохраняя ключи)
$asortArr = $original;
asort($asortArr);
echo "asort (по значениям, ключи сохраняются): ";
print_r($asortArr);
echo "<br>";

// ksort – сортировка по ключам
$ksortArr = $original;
ksort($ksortArr);
echo "ksort (по ключам): ";
print_r($ksortArr);
echo "<br>";

// arsort – обратная сортировка по значениям
$arsortArr = $original;
arsort($arsortArr);
echo "arsort (обратная по значениям): ";
print_r($arsortArr);
echo "<br>";

// krsort – обратная сортировка по ключам
$krsortArr = $original;
krsort($krsortArr);
echo "krsort (обратная по ключам): ";
print_r($krsortArr);
echo "<br><br>";

// ----------------------------------------------------------------------
// 10. Сумма цифр строки '1234567890' без цикла (str_split + array_map + array_sum)
// ----------------------------------------------------------------------
echo "<h2>Задание 10</h2>";
$str = '1234567890';
$digits = str_split($str);            // разбиваем на массив символов
$intDigits = array_map('intval', $digits); // преобразуем каждый в число
$sum = array_sum($intDigits);
echo "Строка: '$str'<br>";
echo "Массив цифр: [" . implode(", ", $intDigits) . "]<br>";
echo "Сумма цифр: $sum<br><br>";

