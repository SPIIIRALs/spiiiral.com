echo "<h2>Задание 6</h2>";
function startsWithHttp($str) {
    $str = (string)$str;
    if (strpos($str, 'http://') === 0 || strpos($str, 'https://') === 0) {
        return 'да';
    }
    return 'нет';
}
echo "Строка 'https://example.com': " . startsWithHttp('https://example.com') . "<br>";
echo "Строка 'ftp://example.com': " . startsWithHttp('ftp://example.com') . "<br><br>";

