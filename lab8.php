// 7. Проверка окончания строки на .png или .jpg
// ----------------------------------------------------------------------
echo "<h2>Задание 7</h2>";
function endsWithImg($str) {
    $str = (string)$str;
    $len = strlen($str);
    if ($len >= 4 && substr($str, -4) === '.png') return 'да';
    if ($len >= 4 && substr($str, -4) === '.jpg') return 'да';
    return 'нет';
}
echo "Файл 'photo.png': " . endsWithImg('photo.png') . "<br>";
echo "Файл 'image.jpeg': " . endsWithImg('image.jpeg') . "<br><br>";

